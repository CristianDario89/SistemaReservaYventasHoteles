    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(!txtidpersona.getText().equals("")){
            int confirmacion=JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar el cliente?","Confirmar",2);
            if(confirmacion==0){
                fcliente func = new fcliente(); //perm usar los metodos
                vcliente dts = new vcliente(); //permite enviar los datos

                dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));  //almacenar el setIdproducto lo q este en la caja d texto
                func.eliminar(dts);
                mostrar("");
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

