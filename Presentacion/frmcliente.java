    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //Validar los campos q son obligatorios ingressar
        if(TxtNombre.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre paraa el cliente no seas pelotudo");
            TxtNombre.requestFocus();
            return;
        }
        if(Txtapaterno.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido");
            Txtapaterno.requestFocus();
            return;
        }
        if(Txtamaterno.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido materno");
            Txtamaterno.requestFocus();
            return;
        }

        if(txtnum_doc.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el numero de documento");
            txtnum_doc.requestFocus();
            return;
        }

        if(Txtcodigo_cliente.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un codigo");
            Txtcodigo_cliente.requestFocus();
            return;
        }

        vcliente dts = new vcliente();
        fcliente func = new fcliente();
//se le envia a vcliente los datos ingresados en las cajas de texto
        dts.setNombre(TxtNombre.getText());
        dts.setApaterno(Txtapaterno.getText());
        dts.setAmaterno(Txtamaterno.getText());

        int seleccionado=cbotipo_doc.getSelectedIndex();
        dts.setTipo_documento((String)cbotipo_doc.getItemAt(seleccionado));
        dts.setNum_documento(txtnum_doc.getText());
        dts.setDireccion(Txtdireccion.getText());
        dts.setTelefono(Txttelefono.getText());
        dts.setEmail(Txtemail.getText());
        dts.setCodigo_cliente(Txtcodigo_cliente.getText());
        if(accion.equals("guardar")){
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, "Cliente registrado satisfoctoriamente");
                mostrar(""); //cadena vacia pa q muestre todos los registros
                inhabilitar();
            }

        }else if (accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));
            if(func.editar(dts)){
                JOptionPane.showMessageDialog(rootPane, "Cliente editado satisfoctoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

